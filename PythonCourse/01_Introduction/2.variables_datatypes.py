
# 2. Variables and Data Types
age = 25       # int
height = 5.9   # float
name = "Alice" # str
is_student = True  # bool - is case sensitive

# Display variable types
print(type(age))   # <class 'int'>
print(type(height)) # <class 'float'>
print(type(name))   # <class 'str'>
print(type(is_student)) # <class 'bool'>
